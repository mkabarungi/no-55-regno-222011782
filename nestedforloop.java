//this program will display  name according to number of loops
public class nestedforloop { //main function

    public static void main(String[] args) { //main function
        
        for(int i=1;i<=3;i++){ //loop to the colum
            for(int j=1;j<=5;j++){ //loop to the lines
                System.out.print("*"); //output
            }
            System.out.println(); //print lines
        }
    }
}
